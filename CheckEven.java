public class CheckEven{
    void checkEven(int n){
     if (n%2==0){
        System.out.println(n);
     }
    }
    public static void main(String[] args) {
        CheckEven a=new CheckEven();
        int n=5;
        a.checkEven(n);
        System.out.println(n);
        
    }
}
    

